
public class LengthOrLengthParentheses {

	public static void main(String[] args) {
		// arrays are a primitive data structure
		String[] words = {"octapus", "dinosaur"};
		System.out.println("The length of the words array is" + words.length );

		// Strings are a class in the Java API, not a primitive
		String word = "promissory";
		System.out.println("The length of the String is " + word.length() );
	}

}
